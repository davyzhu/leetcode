class Solution {
  List<int> getCoprimes(final List<int> nums, final List<List<int>> edges) {
    // fathers map
    var fathers = List<int>.filled(nums.length, -1);
    var connections = List<int>.filled(nums.length, 0);
    var sw = Stopwatch();
    sw.start();
    for (var edge in edges) {
      connections[edge[0]]++;
      connections[edge[1]]++;
    }
    // assert(connections.every((e) => e >= 1));
    var edgeCountDown = edges.length;
    while (edgeCountDown > 0) {
      var i = connections.indexOf(1, 1);
      for (var edge in edges) {
        if (edge[0] == i) {
          fathers[edge[0]] = edge[1];
          connections[edge[0]]--;
          connections[edge[1]]--;
          edgeCountDown--;
          break;
        }
        if (edge[1] == i) {
          fathers[edge[1]] = edge[0];
          connections[edge[0]]--;
          connections[edge[1]]--;
          edgeCountDown--;
          break;
        }
      }
    }
    sw.stop();
    print('create map: ${sw.elapsedMicroseconds}');
    // assert(connections.every((e) => e == 0));
    // coprime ancestor
    sw.reset();
    sw.start();
    var coprimeAncestor = List<int>.filled(nums.length, -1);

    for (var i = 1; i < nums.length; i++) {
      for (var node = fathers[i]; node != -1; node = fathers[node]) {
        if (isCoprime(nums[i], nums[node])) {
          coprimeAncestor[i] = node;
          break;
        }
      }
    }
    sw.stop();
    print('find ancestor: ${sw.elapsedMicroseconds}');
    return coprimeAncestor;
  }

  bool isCoprime(final int i, final int j) {
    return coprime[i - 1][j - 1];
  }
}
